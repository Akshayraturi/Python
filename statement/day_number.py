day_no=int(input("Enter the day number"))
if day_no==1:
    print("Monday")
elif day_no==2:
    print("Tuesday")
elif day_no==3:
    print("Wednesday")
elif day_no==4:
    print("Thursday")
elif day_no==5:
    print("Friday")
elif day_no==6:
    print("Saturday")
elif day_no==7:
    print("Sunday")
else:
    print("Wrong day number")